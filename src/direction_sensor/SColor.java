package direction_sensor;

/**
 *
 * @author txdao
 */
public class SColor {
    private int R;
    private int G;
    private int B;

    public SColor(int R, int G, int B) {
        this.R = R;
        this.G = G;
        this.B = B;
    }

    public SColor() {
    }

    public int getR() {
        return R;
    }

    public void setR(int R) {
        this.R = R;
    }

    public int getG() {
        return G;
    }

    public void setG(int G) {
        this.G = G;
    }

    public int getB() {
        return B;
    }

    public void setB(int B) {
        this.B = B;
    }

}
